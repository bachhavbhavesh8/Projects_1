r.customer_order("Sneha", ["Pasta"])

# Display all
r.print_menu()
r.print_table_reservations()
r.print_customer_orders()

# 20. Write a Python class BankAccount with attributes like account_number, balance, date_of_opening and customer_name, and methods like deposit, withdraw, and check_balance.


class BankAccount:
    def __init__(self, acc_no, name, balance, date):
        self.__account_number = acc_no
        self.__customer_name = name
        self.__balance = balance
        self.__date_of_opening = date

    # Deposit method
    def deposit(self, amount):
        self.__balance += amount
        print(amount, "deposited successfully")

    # Withdraw method
    def withdraw(self, amount):
        if amount <= self.__balance:
            self.__balance -= amount
            print(amount, "withdrawn successfully")
        else:
            print("Insufficient balance ❌")

    # Check balance
    def check_balance(self):
        print("Current Balance:", self.__balance)

    # Display account details
    def display_details(self):
        print("\nAccount Number:", self.__account_number)
        print("Customer Name:", self.__customer_name)
        print("Balance:", self.__balance)
        print("Date of Opening:", self.__date_of_opening)



