# 21. Write a Python class Inventory with attributes like item_id, item_name, stock_count, and price, and methods like add_item, update_item, and check_item_details.

class Inventory:
    def __init__(self):
        self.__item_id = None
        self.__item_name = None
        self.__stock_count = 0
        self.__price = 0

    # Add item
    def add_item(self, item_id, name, stock, price):
        self.__item_id = item_id
        self.__item_name = name
        self.__stock_count = stock
        self.__price = price
        print("Item added successfully ✅")

    # Update item (stock and price)
    def update_item(self, stock=None, price=None):
        if stock is not None:
            self.__stock_count = stock
        if price is not None:
            self.__price = price
        print("Item updated successfully 🔄")

    # Check item details
    def check_item_details(self):
        print("\n--- Item Details ---")
        print("Item ID:", self.__item_id)
        print("Item Name:", self.__item_name)
        print("Stock Count:", self.__stock_count)
        print("Price:", self.__price)



