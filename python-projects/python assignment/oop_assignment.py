# Node class
class Node:
    def __init__(self, data):
        self.data = data
        self.next = None


#  Linked List class
class LinkedList:
    def __init__(self):
        self.head = None

    #  Insert at end
    def insert(self, data):
        new_node = Node(data)

        if self.head is None:
            self.head = new_node
        else:
            temp = self.head
            while temp.next:
                temp = temp.next
            temp.next = new_node

    #  Display list
    def display(self):
        temp = self.head
        while temp:
            print(temp.data, end=" -> ")
            temp = temp.next
        print("None")

    #  Delete a node
    def delete(self, value):
        temp = self.head

        # If head needs to be deleted
        if temp and temp.data == value:
            self.head = temp.next
            temp = None
            return

        # Search for node
        prev = None
        while temp and temp.data != value:
            prev = temp
            temp = temp.next

        # If value not found
        if temp is None:
            print("Value not found")
            return

        # Delete node
        prev.next = temp.next
        temp = None


