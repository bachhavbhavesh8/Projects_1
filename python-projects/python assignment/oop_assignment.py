    def __init__(self, name, emp_id, salary, department):
        self.__emp_name = name
        self.__emp_id = emp_id
        self.__emp_salary = salary
        self.__emp_department = department

    # Method to calculate salary with overtime
    def calculate_emp_salary(self, hours_worked):
        if hours_worked > 50:
            overtime = hours_worked - 50
            overtime_amount = overtime * (self.__emp_salary / 50)
            total_salary = self.__emp_salary + overtime_amount
        else:
            total_salary = self.__emp_salary

        return total_salary

    # Method to assign new department
    def assign_department(self, new_department):
        self.__emp_department = new_department

    # Method to display details
    def print_employee_details(self):
        print("\nEmployee ID:", self.__emp_id)
        print("Name:", self.__emp_name)
        print("Salary:", self.__emp_salary)
        print("Department:", self.__emp_department)


