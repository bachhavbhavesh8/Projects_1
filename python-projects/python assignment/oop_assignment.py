# 19. Write a Python class Restaurant with attributes like menu_items, book_table, and customer_orders, and methods like add_item_to_menu, book_tables, and customer_order.

# 	Perform the following tasks now:

# 	Now add items to the menu.
# 	Make table reservations.
# 	Take customer orders.
# 	Print the menu.
# 	Print table reservations.
# 	Print customer orders.
# 	Note: Use dictionaries and lists to store the data.

class Restaurant:
    def __init__(self):
        self.menu_items = {}          # dictionary
        self.booked_tables = []       # list
        self.customer_orders = {}     # dictionary

    def add_item_to_menu(self, item, price):
        self.menu_items[item] = price

    def book_tables(self, table_number):
        self.booked_tables.append(table_number)

    def customer_order(self, customer_name, items):
        self.customer_orders[customer_name] = items

    def print_menu(self):
        print("\n--- MENU ---")
        for item, price in self.menu_items.items():
            print(item, ":", price)

    # Print  reservations
    def print_table_reservations(self):
        print("\n--- TABLE RESERVATIONS ---")
        for table in self.booked_tables:
            print("Table", table)

    # Print customer orders
    def print_customer_orders(self):
        print("\n--- CUSTOMER ORDERS ---")
        for customer, items in self.customer_orders.items():
            print(customer, "ordered:", items)


r = Restaurant()

