# Queue class
class Queue:

    def __init__(self):
        self.items = []

    # Enqueue (add elements)
    def enqueue(self, item):
        self.items.append(item)
        print(item, "enqueued into queue")

    # Dequeue (remove element)
    def dequeue(self):
        # Check if queue is empty
        if len(self.items) == 0:
            print("Queue is empty (Underflow)")
        else:
            # Remove element from front (index 0)
            removed = self.items.pop(0)
            print(removed, "dequeued from queue")

    #  Display queue
    def display(self):
        if len(self.items) == 0:
            print("Queue is empty")
        else:
            print("Queue elements (front → rear):")
            for item in self.items:
                print(item)


