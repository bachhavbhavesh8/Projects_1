#  Challenge: Student Marks Analyzer

# Create a Python program that allows a user to input student names along with their marks and then calculates useful statistics.
# Your program should:
# 1. Let the user input multiple students with their marks (name + integer score).
# 2. After input is complete, display:
#    - Average marks
#    - Highest marks and student(s) who scored it
#    - Lowest marks and student(s) who scored it
#    - Total number of students

# Bonus:
# - Allow the user to enter all data first, then view the report
# - Format output clearly in a report-style layout
# - Prevent duplicate student names

def student_marks_analyzer():
    students = {}   # dictionary to store name: marks

    print(" Student Marks Entry ")

    while True:
        name = input("Enter student name (or type 'done' to finish): ").strip()

        if name.lower() == 'done':
            break

        # Prevent duplicate names
        if name in students:
            print(" Student already exists! Try a different name.")
            continue

        try:
            marks = int(input("Enter marks: "))
            students[name] = marks
        except ValueError:
            print("Invalid input! Marks must be an integer.")

    # If no data entered
    if len(students) == 0:
        print("\nNo student data entered.")
        return

    # Calculations
    total_students = len(students)
    total_marks = sum(students.values())
    average = total_marks / total_students

    highest_marks = max(students.values())
    lowest_marks = min(students.values())

    # Find students with highest and lowest marks
    highest_students = []
    lowest_students = []

    for name, marks in students.items():
        if marks == highest_marks:
            highest_students.append(name)
        if marks == lowest_marks:
            lowest_students.append(name)

    # Report
    print("\n Student Marks Report ")
    print(f"Total Students : {total_students}")
    print(f"Average Marks  : {average:.2f}")

    print(f"\nHighest Marks  : {highest_marks}")
    print("Topper(s)      :", ", ".join(highest_students))

    print(f"\nLowest Marks   : {lowest_marks}")
    print("Lowest Scorer(s):", ", ".join(lowest_students))



# Run program
student_marks_analyzer()

#  Challenge: CLI Contact Book (CSV-Powered)

# Create a terminal-based contact book tool that stores and manages contacts using a CSV file.

# Your program should:
# 1. Ask the user to choose one of the following options:
#    - Add a new contact
#    - View all contacts
#    - Search for a contact by name
#    - Exit
# 2. Store contacts in a file called `contacts.csv` with columns:
#    - Name
#    - Phone
#    - Email
# 3. If the file doesn't exist, create it automatically.
# 4. Keep the interface clean and clear.

# Example:
